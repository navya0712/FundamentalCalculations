package com.indus.fundamental.assignment;

/**
 * Description:Calculates power of a number
 */
public class PowerOfNumberCalculator {

	/**
	 * Description: Calculates power of a number
	 * 
	 * @param base
	 * @param exponent
	 * @return power
	 */
	public double computePower(double base, double exponent) {
		double result = 0.0;
		{
			result = Math.pow(base, exponent);

		}
		return result;

	}

}
